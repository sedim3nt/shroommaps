export function getRetailerBySlug(slug: string): Retailer | undefined {
  return retailers.find((r) => r.slug === slug);
}

export function getFeaturedRetailers(): Retailer[] {
  return retailers.filter((r) => r.featured);
}

export function searchRetailers(query: string, vertical?: string): Retailer[] {
  let results = retailers;
  if (vertical) {
    results = results.filter((r) =>
      r.verticals.includes(vertical as Retailer["verticals"][number])
    );
  }
  if (query) {
    const q = query.toLowerCase();
    results = results.filter(
      (r) =>
        r.name.toLowerCase().includes(q) ||
        r.city.toLowerCase().includes(q) ||
        r.description.toLowerCase().includes(q) ||
        r.tags.some((t) => t.toLowerCase().includes(q))
    );
  }
  return results;
}
